void main(){
  // initialization
  //while -> while(condition){
  //                    statements
  //                    incre/decre
  //              }
  int i = 1;
  while(i <= 10){
    print(i);
    i++;
  }   // i = 1  1<=10 print 1    i++ = 2

  print("");
  print("do while");
  int j = 0;
  do{
    print(j);
    j++;
  }while(j <= 10);
      /// j = 0  print j = 0  j++ = 1  1<= 10
}